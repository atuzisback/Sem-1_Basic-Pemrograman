public class D_ForBreakGcr2 {
    public static void main(String[] args) {

        // Loop dari 1 sampai 100, berhenti jika i == 50
        for (int i = 1; i <= 100; i++) {
            System.out.println(i);
            if (i == 50) {
                break; // keluar dari loop saat mencapai 50
            }
        }
     }
}

