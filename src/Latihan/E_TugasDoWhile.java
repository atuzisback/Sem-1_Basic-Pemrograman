public class E_TugasDoWhile {
    public static void main(String[] args) {
        
        // Inisialisasi variabel
        int angka = 1; 

        do {
            // Pernyataan yang akan dieksekusi
            System.out.println("Angka: " + angka); 
            
            // Memperbarui variabel (menambah nilai)
            angka++; 
            
            // Kondisi perulangan
        } while (angka <= 5); 
    }
}
   
