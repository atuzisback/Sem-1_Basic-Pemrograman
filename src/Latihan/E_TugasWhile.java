public class E_TugasWhile {
    public static void main(String[] args) {
        
        // Inisialisasi variabel angka
        int angka = 1; 

        // Loop akan berjalan selama nilai 'angka' kurang dari atau sama dengan 5
        while (angka <= 5) {
            
            // Cetak angka saat ini
            System.out.println("Angka saat ini: " + angka); 
            
            // Tingkatkan nilai angka sebanyak 1 setelah setiap iterasi
            angka++; 
        
            // Pesan setelah loop selesai
            System.out.println("Perulangan selesai!"); 
        }
    }
}
    
